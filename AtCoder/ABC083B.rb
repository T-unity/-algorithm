'1以上N以下の整数のうち、10進法での各桁の和がA以上B以下であるものの総和を求めてください。'

# 制約
'1 ≤ N ≤ 10の4乘
 1 ≤ A ≤ B ≤ 36
 入力はすべて整数である'

# Nは1以上10,000未満
# Aの最小値は1
# Bの最大値は36(9999の時)

'入力は N,A,B'

'N = 100'
'A = 4'
'B = 16'

'出力 4554'

'N = 20'
'A = 2'
'B = 5'

'当てはまる値 2,3,4,5,11,12,13,14,20'

'出力 84'

N,A,B = gets.split(" ").map(&:to_i)

sum = 0
for n in 1..N
  num = (n/10000) + (n/1000%10) +(n/100%10) + (n/10%10) + (n%10)
  sum += n if num >= A && num <= B
end

puts sum